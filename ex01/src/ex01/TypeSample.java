package ex01;

public class TypeSample {
	public static void run() {
		// 변수타입
		// 숫자
		int num1 = 80;
		float num2 = 80.5f;
		double num3 = 80.5;
		
		// 문자
		char str1 = 'A';
		String str2 = "ABCD";
		
		// 참or거짓
		boolean isEnd = true;
		
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("num3 = " + num3);
		System.out.println("str1 = " + str1);
		System.out.println("str2 = " + str2);
	}

}
